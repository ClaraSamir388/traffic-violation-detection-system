import numpy as np

# Calculate the number of vehicles
total_vehicles = len(speeds_history)
valid_tracks = 0
stable_tracks = 0
smooth_tracks = 0
logical_tracks = 0
track_mean_speeds = [] # List to store mean speeds of each track

for tid, speeds in speeds_history.items():
    speeds = np.array(list(speeds)) # Convert deque to numpy array
    if len(speeds) < 5:
        continue
    valid_tracks += 1

    # Speed stability
    std_speed = np.std(speeds)
    if std_speed < 8:
        stable_tracks += 1

    # Speed smoothness
    if np.all(np.diff(speeds) < 30):
        smooth_tracks += 1

    # Logical speeds
    if np.all((speeds >= 0) & (speeds <= 180)):
        logical_tracks += 1

    # Store the mean speed of this track
    track_mean_speeds.append(np.mean(speeds))


if valid_tracks > 0:
    stability_score = stable_tracks / valid_tracks
    smoothness_score = smooth_tracks / valid_tracks
    logic_score = logical_tracks / valid_tracks
    # Calculate the average movement score from the list of mean speeds
    movement_score = sum(np.array(track_mean_speeds) > 3) / valid_tracks


    # Final score (balanced)
    accuracy = (0.35*stability_score +
                0.35*smoothness_score +
                0.2*movement_score +
                0.1*logic_score)
else:
    accuracy = 0

