import os, cv2, random, csv, numpy as np
from collections import defaultdict, deque
from shapely.geometry import Point, Polygon
import supervision as sv
from ultralytics import YOLO
from IPython.display import Video, display

# ======================================
#  STEP 1: Load Video
# ======================================
VIDEO_FOLDER = "/kaggle/input/driving-video-with-object-tracking/bdd100k_videos_train_00/bdd100k/videos/train"

if not os.path.exists(VIDEO_FOLDER):
    raise FileNotFoundError(" Video folder not found!")

videos = [f for f in os.listdir(VIDEO_FOLDER) if f.lower().endswith(('.mp4', '.mov'))]
VIDEO_IN = os.path.join(VIDEO_FOLDER, random.choice(videos))
VIDEO_OUT = "enhanced_output.avi"

print(f"🎲 Selected video: {os.path.basename(VIDEO_IN)}")

cap = cv2.VideoCapture(VIDEO_IN)
fps = int(cap.get(cv2.CAP_PROP_FPS))
W, H = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH)), int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
out = cv2.VideoWriter(VIDEO_OUT, cv2.VideoWriter_fourcc(*'XVID'), fps, (W, H))
print(f" Video loaded successfully ({W}x{H}, {fps} FPS)")

# ======================================
#  STEP 2: YOLOv8 + Tracker + Constants
# ======================================
model = YOLO("yolov8x.pt")
tracker = sv.ByteTrack()

VEHICLE_CLASSES = {"car", "truck", "bus", "motorbike", "motorcycle"}
SPEED_LIMIT_KMH = 80
LANE_CHANGE_IS_VIOLATION = True

# ======================================
#  STEP 3: Homography & Lanes
# ======================================
image_points = np.array([[250,700],[1050,700],[750,400],[550,400]],dtype=np.float32)
world_points = np.array([[0,0],[3.5,0],[3.5,30],[0,30]],dtype=np.float32)
H_matrix, _ = cv2.findHomography(image_points, world_points)

def pixel_to_world(x, y):
    p = np.array([[[x, y]]], np.float32)
    return cv2.perspectiveTransform(p, H_matrix)[0][0]

lane_right_poly = Polygon([(50,300),(600,300),(600,720),(50,720)])
lane_left_poly  = Polygon([(600,300),(1270,300),(1270,720),(600,720)])

def draw_lanes(frame):
    cv2.polylines(frame, [np.array(lane_right_poly.exterior.coords, np.int32)], True, (0,255,0), 2)
    cv2.polylines(frame, [np.array(lane_left_poly.exterior.coords, np.int32)], True, (255,0,0), 2)
    return frame

def point_in_lane(point, poly):
    return poly.contains(Point(point))

# ======================================
#  STEP 4: Memory & Counters
# ======================================
last_centers = {}
last_speed = {}
speeds_history = defaultdict(lambda: deque(maxlen=20))
lane_of_track = {}
viol_speed_ids = set()
viol_lane_ids = set()
frame_age = defaultdict(int)
records = []

# ======================================
# 🚗 STEP 5: Main Loop
# ======================================
print(" Starting detection & tracking...")
frame_idx = 0

while True:
    ok, frame = cap.read()
    if not ok:
        break

    results = model(frame, conf=0.25, iou=0.45, imgsz=960, augment=True, verbose=False)[0]
    detections = sv.Detections.from_ultralytics(results)
    tracked = tracker.update_with_detections(detections)
    frame = draw_lanes(frame)

    for i in range(len(tracked.xyxy)):
        box = tracked.xyxy[i]
        cls = tracked.class_id[i]
        tid = tracked.tracker_id[i]
        if tid is None: continue

        label = model.model.names[int(cls)]
        if label not in VEHICLE_CLASSES: continue

        x1, y1, x2, y2 = map(int, box)
        cx, cy = (x1 + x2)//2, (y1 + y2)//2
        center = (cx, cy)
        frame_age[tid] += 1

        # Lane checking
        in_right = point_in_lane(center, lane_right_poly)
        in_left = point_in_lane(center, lane_left_poly)
        curr_lane = "R" if in_right else ("L" if in_left else "U")
        prev_lane = lane_of_track.get(tid, curr_lane)
        lane_of_track[tid] = curr_lane

        if LANE_CHANGE_IS_VIOLATION and curr_lane != prev_lane and prev_lane in ("L","R") and curr_lane in ("L","R"):
            viol_lane_ids.add(tid)

        # Speed calculation
        if tid in last_centers:
            prev_world = pixel_to_world(*last_centers[tid])
            curr_world = pixel_to_world(cx, cy)
            dist_m = np.linalg.norm(np.array(curr_world) - np.array(prev_world))
            if 0.2 < dist_m < 10:
                speed_kmh = dist_m * fps * 3.6
                last_s = last_speed.get(tid, 0)
                if abs(speed_kmh - last_s) > 40:
                    speed_kmh = last_s
                speeds_history[tid].append(speed_kmh)
                avg_speed = np.mean(speeds_history[tid])
                last_speed[tid] = speed_kmh
            else:
                avg_speed = 0
        else:
            avg_speed = 0.0

        last_centers[tid] = center

        # Violations
        violation_type = "None"
        color = (0,255,0)
        top_label = f"ID {tid}"

        if avg_speed < 8:
            avg_speed = 0
        elif frame_age[tid] > 5 and avg_speed > SPEED_LIMIT_KMH:
            viol_speed_ids.add(tid)
            color = (0,0,255)
            top_label = f" SPEED VIOLATION (ID {tid})"
            violation_type = "Speed"
        elif tid in viol_lane_ids:
            color = (255,0,0)
            top_label = f" LANE VIOLATION (ID {tid})"
            violation_type = "Lane"

        # Draw on frame
        cv2.rectangle(frame, (x1,y1), (x2,y2), color, 2)
        cv2.putText(frame, top_label, (x1, max(20, y1 - 25)), cv2.FONT_HERSHEY_SIMPLEX, 0.7, color, 2)
        cv2.putText(frame, f"{label} | {avg_speed:.1f} km/h", (x1, y2 + 25),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.6, color, 2)

        # Save record for CSV
        records.append({
            "Frame": frame_idx,
            "TrackID": tid,
            "Vehicle": label,
            "Speed (km/h)": round(avg_speed, 2),
            "Violation": violation_type,
            "Lane": curr_lane
        })

    out.write(frame)
    frame_idx += 1
    if frame_idx % 50 == 0:
        print(f" Processed {frame_idx} frames")

cap.release()
out.release()
print(f" Done! Total frames: {frame_idx}")

# ======================================
# 📊 STEP 6: Save CSV Report
# ======================================
csv_filename = "traffic_violations_report.csv"
with open(csv_filename, "w", newline="") as f:
    writer = csv.DictWriter(f, fieldnames=["Frame","TrackID","Vehicle","Speed (km/h)","Violation","Lane"])
    writer.writeheader()
    writer.writerows(records)
print(f"📄 CSV report saved as {csv_filename} ({len(records)} records)")

